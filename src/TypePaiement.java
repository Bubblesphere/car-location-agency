
public class TypePaiement {
	private int Id;
	private String Nom;
	private Boolean Caution;
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getNom() {
		return Nom;
	}
	public void setNom(String nom) {
		Nom = nom;
	}
	public Boolean getCaution() {
		return Caution;
	}
	public void setCaution(Boolean caution) {
		Caution = caution;
	}
}
